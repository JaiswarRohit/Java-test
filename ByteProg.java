class ByteProg
{
	public static void main(String[] args) 
	{
		byte b = -128 ;
		short s= 32767;
		System.out.println(b);
		System.out.println(s);

		float f = 1/3f;
		double d = 1/3f;
		System.out.println(f);
		System.out.println(d);

		boolean a = true;
		boolean c = false;
		System.out.println(a);
		System.out.println(c);

		char ch = 99;
		char ch1 = 'B';
		System.out.println(ch);
		System.out.println(ch1);
	}
}

